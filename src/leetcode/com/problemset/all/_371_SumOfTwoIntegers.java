public class _371_SumOfTwoIntegers {
	/**
	 * Problem : https://leetcode.com/problems/sum-of-two-integers/
	 * @param a
	 * @param b
	 * @return
	 */
    public int getSum(int a, int b) {
        return Math.addExact(a,b);
    }

}
